#Create salesperson_revenue dictionary
salesperson_revenue={"Ben":0,
"Omer":0,
"Karen":0,
"Bora":0,
"Rose":0
}

#Define enter_revenue function
def enter_revenue(name,revenue):
  global salesperson_revenue
  salesperson_revenue[name]+=revenue

